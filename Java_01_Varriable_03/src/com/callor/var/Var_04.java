package com.callor.var;

public class Var_04 {

	public static void main(String[] args) {
		int num1 = 10;

		num1 += 10; // num1 = num1 + 10;
		num1 *= 10; // num1 = num1 * 10;
		num1 -= 10; // num1 = num1 - 10;
		num1 /= 10; // num1 = num1 / 10;
		num1 %= 10; // num1 = num1 % 10;

		num1++; // num1 = num1 + 1;
		num1--; // num1 = num1 - 1;

		System.out.println(num1);
	}
}
