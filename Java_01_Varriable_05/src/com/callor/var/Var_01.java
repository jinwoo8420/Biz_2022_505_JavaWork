package com.callor.var;

public class Var_01 {

	public static void main(String[] args) {
		double num1 = Math.random() * 1000;
		double num2 = Math.random() * 1000;
		
		System.out.println(num1);
		System.out.println(num2);
		
		System.out.println();
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
	}

}
