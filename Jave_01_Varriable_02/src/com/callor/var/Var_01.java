package com.callor.var;

public class Var_01 {

	public static void main(String[] args) {
		/*
		 * 변수명은 키워드 등으로 사용 불가
		 * 같은 변수명은 { } 내부에서 두번 이상 선언 불가
		 */
		int num1 = 55, num2 = 33; 

		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
	}

}
